package RevisaoAritimetica;

public class Expressao {
	public static void main(String[]args){
		double valor = 4.0 + 3.0*2.0;
	      System.out.println("Expressão=4.0 + 3.0 * 2.0 = " + valor); 
	      valor = 2.0 * 3.0 + 4.0;
	      System.out.println("Expressão=2.0 * 3.0 + 4.0 = " + valor); 
	      valor = 2.0 * (3.0 + 4.0);
	      System.out.println("Expressão=2.0 * (3.0 + 4.0) = " + valor); 

	}
}
